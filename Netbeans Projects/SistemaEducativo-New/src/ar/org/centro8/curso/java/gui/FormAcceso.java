    private void btnIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIngresarMouseClicked
        // Ingreso
        String usuario="admin";
        String clave="1234";
        String pass= new String(txtClave.getPassword());
        
        if (txtUsuario.getText().equals(usuario) && pass.equals(clave)) {
            FormPrincipal formP = new FormPrincipal();
            formP.setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(this, "Bienvenido");
        } else {
            if (txtUsuario.getText().equals(usuario) && !pass.equals(clave)) {
                JOptionPane.showMessageDialog(this, "Clave Invalida");
            }
            if (!txtUsuario.getText().equals(usuario) && pass.equals(clave)) {
                JOptionPane.showMessageDialog(this, "Usuario Invalida");
            }
        }
    }//GEN-LAST:event_btnIngresarMouseClicked

